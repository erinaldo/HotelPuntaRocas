using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;
using CapaDatos;
using CapaEntidades;

namespace CapaNegocio
{
    public class CategoriaProductoNegocio : GenericClass<SysHotelEntities, Categoria_producto>, IDisposable
    {
        private SysHotelEntities _dataContext;

        public CategoriaProductoNegocio()
        {
            _dataContext = new SysHotelEntities();
        }

        public void Dispose()
        {
            if (_dataContext == null) return;
            _dataContext.Dispose();
            _dataContext = null;
        }
    }
}